#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*src;
	size_t		i;

	i = 0;
	src = s;
	while (i < n)
	{
		if (src[i] == c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
